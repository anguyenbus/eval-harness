"""
Stub RAG ingestion for testing and development.

This stub returns hardcoded schema-conformant rag_query_output for any input.
It is NOT a real RAG system - it exists purely to unblock eval harness development.
"""

from pathlib import Path
from typing import Any


def query(question: str, corpus_dir: Path) -> dict[str, Any]:
    """
    Query a RAG system and return schema-conformant rag_query_output.

    This is a STUB implementation that returns hardcoded output regardless
    of input. The output validates against rag_query_output.schema.json.

    Args:
        question: The question text (ignored in stub).
        corpus_dir: Path to document corpus (ignored in stub).

    Returns:
        Dictionary conforming to rag_query_output.schema.json with:
        - schema_version: "1.0.0"
        - system_version.pipeline_version: "0.0.1-stub"
        - One retrieved chunk with rank=0
        - Stub answer with answer_supported=False
        - Empty citations list

    """
    # Generate stable IDs from question text
    query_hash = hash(question) % 10000

    output = {
        "schema_version": "1.0.0",
        "system_version": {
            "pipeline_version": "0.0.1-stub",
            "parser_version": "0.0.1",
            "chunker_version": "0.0.1",
            "embedder_model": "text-embedding-3-large",
            "retriever_version": "0.0.1",
            "reranker_model": None,
            "generator_model": "claude-opus-4-7",
        },
        "query": {
            "query_id": f"query_{query_hash:04d}",
            "text": question,
            "metadata": {},
        },
        "answer": {
            "text": (
                "This is a stub answer generated by the eval-harness stub RAG system. "
                "The stub does not actually retrieve or generate content based on the "
                "corpus or question."
            ),
            "answer_supported": False,
            "citations": [],
        },
        "retrieved_chunks": [
            {
                "chunk_id": f"chunk_{query_hash:04d}_001",
                "rank": 0,
                "score": 0.95,
                "retrieval_stage": "initial",
                "doc_id": "stub_doc_001",
                "element_ids": [f"elem_{query_hash:04d}_001"],
                "char_span": [0, 150],
                "page_indices": [0],
                "text": "This is a stub retrieved chunk. In a real RAG system, this would contain relevant content from the corpus.",
            }
        ],
        "timings_ms": {
            "retrieval": 50,
            "rerank": 0,
            "generation": 100,
            "total": 150,
        },
    }

    return output
