"""
Stub parser for testing and development.

This stub returns hardcoded schema-conformant parser_output for any input PDF.
It is NOT a real parser - it exists purely to unblock eval harness development.
"""

# NOTE: Using hashlib instead of reading PDF content since this is a stub
import hashlib
from pathlib import Path
from typing import Any


def _get_doc_id(pdf_path: Path) -> str:
    """Generate a stable doc_id from PDF filename."""
    # Use filename as doc_id for stub (real parser would hash content)
    return pdf_path.stem


def _get_sha256(pdf_path: Path) -> str:
    """Generate SHA-256 hash of PDF file."""
    # Real stub should actually hash the file
    content = pdf_path.read_bytes()
    return hashlib.sha256(content).hexdigest()


def parse(pdf_path: Path) -> dict[str, Any]:
    """
    Parse a PDF file and return schema-conformant parser_output.

    This is a STUB implementation that returns hardcoded output regardless
    of input content. The output validates against parser_output.schema.json.

    Args:
        pdf_path: Path to PDF file (any PDF will produce stub output).

    Returns:
        Dictionary conforming to parser_output.schema.json with:
        - schema_version: "1.0.0"
        - parser_version: "0.0.1-stub"
        - One heading element
        - One paragraph element
        - STUB_OUTPUT warning code

    """
    doc_id = _get_doc_id(pdf_path)
    sha256 = _get_sha256(pdf_path)

    # Hardcoded stub elements
    heading_text = "Sample Document Heading"
    paragraph_text = (
        "This is a stub paragraph generated by the eval-harness stub parser. "
        "It does not reflect the actual content of the PDF document."
    )

    heading_end = len(heading_text)
    paragraph_end = heading_end + 1 + len(paragraph_text)  # +1 for newline

    output = {
        "schema_version": "1.0.0",
        "parser_version": "0.0.1-stub",
        "parsed_at": "2025-01-01T00:00:00Z",
        "source": {
            "doc_id": doc_id,
            "filename": pdf_path.name,
            "mime_type": "application/pdf",
            "sha256": sha256,
            "page_count": 1,
            "language": "en",
        },
        "pages": [
            {
                "page_index": 0,
                "width": 612,  # Standard US Letter width in points
                "height": 792,  # Standard US Letter height in points
                "rotation": 0,
            }
        ],
        "elements": [
            {
                "element_id": f"{doc_id}_heading_001",
                "type": "heading",
                "level": 1,
                "page_index": 0,
                "char_span": [0, heading_end],
                "text": heading_text,
                "content": {"kind": "text"},
                "bbox": {
                    "x0": 72,  # 1 inch margin
                    "y0": 720,
                    "x1": 540,
                    "y1": 700,
                },
                "parent_id": None,
            },
            {
                "element_id": f"{doc_id}_paragraph_001",
                "type": "paragraph",
                "page_index": 0,
                "char_span": [heading_end + 1, paragraph_end],
                "text": paragraph_text,
                "content": {"kind": "text"},
                "bbox": {
                    "x0": 72,
                    "y0": 690,
                    "x1": 540,
                    "y1": 650,
                },
                "parent_id": None,
            },
        ],
        "warnings": [
            {
                "code": "STUB_OUTPUT",
                "message": (
                    "This output was generated by the stub parser, not a real "
                    "document parser. Do not use for evaluation metrics."
                ),
            }
        ],
    }

    return output
